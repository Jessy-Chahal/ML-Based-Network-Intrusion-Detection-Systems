"""
Retrain baseline models (RF, XGBoost, MLP + majority-voting ensemble)
on an adversarial training dataset from data/adversarial/. 

Validation uses the original split from data/splits/<dataset>.npz (unchanged).

Default mode - full adversarial data (all three attack families combined):
    python src/train_adversarial.py --dataset cicids2017
    python src/train_adversarial.py --all

Partial mode - trained on a single attack family only:
    python src/train_adversarial.py --dataset cicids2017 --attack a
    python src/train_adversarial.py --all --attack b

Partial mode reads from data/adversarial/adv_train_{stem}_attack_{a|b|c}_only.npz
(generated by src/gen_adversarial_partial.py) and saves models/results under a
distinct prefix so they never overwrite the fully-trained adversarial artifacts.

prefixes:
  default -> adv_{rf,xgb,mlp}_{dataset}.*   (same as before)
  --attack a -> adv_a_only_{rf,xgb,mlp}_{dataset}.*
  --attack b -> adv_b_only_{rf,xgb,mlp}_{dataset}.*
  --attack c -> adv_c_only_{rf,xgb,mlp}_{dataset}.*

Metrics JSON:
  default -> results/retrained_adversarial_metrics_{dataset}.json
  --attack a -> results/retrained_adversarial_attack_a_only_metrics_{dataset}.json
"""
import json
import argparse
from pathlib import Path
from scipy.stats import mode
from typing import Optional
import sys

import numpy as np
import joblib
from sklearn.ensemble import RandomForestClassifier
from sklearn.preprocessing import StandardScaler
from sklearn.metrics import (
    accuracy_score,
    precision_recall_fscore_support,
    confusion_matrix,
)
from sklearn.utils.class_weight import compute_class_weight
import xgboost as xgb

from tensorflow.keras import Sequential
from tensorflow.keras.layers import Dense
from tensorflow.keras.callbacks import EarlyStopping

